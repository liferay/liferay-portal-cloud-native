import org.junit.runner.RunWith;

/**
 * @author Carolina Barbosa
 */
@RunWith(Arquillian.class)
public class MyWorkflowTaskControlPanelEntryTest {

	@ClassRule
	@Rule
	public static final LiferayIntegrationTestRule liferayIntegrationTestRule =
		new LiferayIntegrationTestRule();

	@Before
	public void setUp() throws Exception {
		_group1 = GroupTestUtil.addGroup();
		_group2 = GroupTestUtil.addGroup();
	}

	@Test
	public void testHasAccessPermission() throws Exception {
		User user = UserTestUtil.addUser();

		PermissionChecker permissionChecker =
			PermissionCheckerFactoryUtil.create(user);

		try (ContextUserReplace contextUserReplace = new ContextUserReplace(
				user, permissionChecker)) {

			RoleTestUtil.addResourcePermission(
				RoleConstants.USER, PortletKeys.MY_WORKFLOW_TASK,
				ResourceConstants.SCOPE_GROUP,
				String.valueOf(_group1.getGroupId()),
				ActionKeys.ACCESS_IN_CONTROL_PANEL);

			Assert.assertTrue(
				_controlPanelEntry.hasAccessPermission(
					permissionChecker, _group1, _portlet));
			Assert.assertFalse(
				_controlPanelEntry.hasAccessPermission(
					permissionChecker, _group2, _portlet));
		}
	}

	@Inject(filter = "jakarta.portlet.name=" + PortletKeys.MY_WORKFLOW_TASK)
	private ControlPanelEntry _controlPanelEntry;

	@DeleteAfterTestRun
	private Group _group1;

	@DeleteAfterTestRun
	private Group _group2;

	@Inject(filter = "jakarta.portlet.name=" + PortletKeys.MY_WORKFLOW_TASK)
	private Portlet _portlet;

}