import ClayButton from '@clayui/button';
import {ClayInput} from '@clayui/form';
import ClayLayout from '@clayui/layout';
import {IFDSState} from '@liferay/frontend-data-set-web';
import {useLiferayState} from '@liferay/frontend-js-state-web/react';
import React, {useEffect, useMemo, useState} from 'react';

import {getAtom} from '../utils/getAtom';

const CLASSIC_FDS_ATOM_KEY =
	'com_liferay_frontend_data_set_sample_web_internal_portlet_FDSSamplePortlet-classic_fdsState';

/**
 * This fragment highlights sync with FDS from isolated context, if there is
 * no recommended sync method on the page. The key used is the key
 * auto-generated by FDS.
 *
 * This is not the recommended sync method. For the recommended method, see
 * "AdvancedFilters" fragment.
 */
const ClassicSearch = () => {
	const memoizedAtom = useMemo(
		() => getAtom({key: CLASSIC_FDS_ATOM_KEY}),
		[]
	);

	const [fdsState, setFDSState] = useLiferayState<IFDSState>(memoizedAtom);

	const [query, setQuery] = useState(fdsState.search.query ?? '');

	useEffect(() => {
		setQuery(fdsState.search.query);
	}, [fdsState]);

	return (
		<ClayLayout.ContainerFluid>
			<ClayInput.Group className="pt-2">
				<ClayInput.GroupItem>
					<ClayInput
						className="form-control"
						component="input"
						onChange={({target: {value}}) => setQuery(value)}
						placeholder="Search in Classic tab of Frontend Data Set Sample"
						value={query}
					/>
				</ClayInput.GroupItem>

				<ClayInput.GroupItem>
					<ClayButton
						data-qa-id="classicSearchFDSSampleButton"
						onClick={() => {
							setFDSState({
								...(fdsState as IFDSState),
								search: {query},
							});
						}}
					>
						Search
					</ClayButton>
				</ClayInput.GroupItem>
			</ClayInput.Group>
		</ClayLayout.ContainerFluid>
	);
};

export default ClassicSearch;
